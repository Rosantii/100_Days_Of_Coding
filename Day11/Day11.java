
package day9;

public class Day11 {
    public static void main(String[] args) {
        
        // operator penugasan
       
        //pengisian nilai
        int b = 2;
        int c = 5;
        
        //penambahan
        b += c;
        System.out.println("Penambahan : " +b);
        
        //pengurangan
        b -= c;
        System.out.println("Pengurangan : " +b);
        
        //perkalian
        b *= c;
        System.out.println("Perkalian : " +b);
        
        //pembagian
        b /= c;
        System.out.println("Pembagian : " +b);
        
        //sisa bagi
        b %= c;
        System.out.println("Sisa bagi : " +b);
        
    }
}
