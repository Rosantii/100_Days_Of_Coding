package day9;

public class Day26 {
    public static void main(String[] args) {
        
        // Increment dengan +=
        for (int i = 0; i < 10; i += 2) {
            System.out.println(i);
            
        }
        //Decrement dengan -=
        for (int i = 10; i > 0; i -= 2) {
            System.out.println(i);
            
        }
        // Increment dengan ++ 
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
            
            
        }
        //Decrement dengan --
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
            
        }
}

    }
    

