
package day9;


public class Day27 {
    public static void main(String[] args) {
            //perulangan while
            int a =1;
            while(a<=5){
                System.out.println(a);
                a++;
            }
            
            //perulangan do while
            int b = 1;
            do {
                System.out.println(b);
                b++;
            } while (b <= 5);
    }
}
